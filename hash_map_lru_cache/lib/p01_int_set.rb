class MaxIntSet
  
  def initialize(max)
    @store = Array.new(max, false)
    @max = max
  end

  def insert(num)
    @store[num] = true if is_valid?(num)
  end

  def remove(num)
    @store[num] = false if is_valid?(num)
  end

  def include?(num)
    @store[num]
  end

  private

  def is_valid?(num)
    valid = (num <= @max) && (num >= 0)
    raise "Out of bounds" unless valid
    valid
  end

